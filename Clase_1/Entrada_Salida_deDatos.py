# PRINT - Mostrar
# Con comillas dobles o  simples
print ('Que onda loco')
print ("Que onda loco?")
# Pero si quiero mostrar comillas dentro de mi print
print("Que onda 'loco'?")
# Con \n hacemos un salto de linea y con \t una tabulacion
print ("Que onda loco?\n")
print ("Que onda \t loco?")
# Podemos concatenar dentro delPrint con +
print ("Que onda loco?" +" "+ "Todo bien pa")
# O podemos dejar un espacio entre variables con SEP
print ("Que onda loco?", "Todo bien pa", sep= " ")
# SEP sirve para concatenar cualquier variable entre variables
print ("Ri","er es de la ", "",sep= "b")

